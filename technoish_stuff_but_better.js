setcps(180/60/4)

$: note(irand(8))
  .struct("{0 0 0 1 1}%4")
  .sound("gm_epiano1:11")
  .scale("a#5:blues")
  .delay(.5)
  .decay(.3)
  .rib(12, 1)
  .slow(2)
  .gain(.4)
  ._pianoroll()

$: irand(8)
  .n()
  .struct("{1 1 1 1 1 1}%8")
  .sound("gm_epiano1")
  .decay(.1)
  .scale("g5:minor")
  .clip(2)
  .lpf(cat(2000, 5000))
  .gain(rand.range(0.3, 0.4))
  .rib(7, 2)
  ._pianoroll();

$: irand(8).n().s("808").scale("c1:minor")
